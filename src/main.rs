    fn walk_leaves(cmd: &clap::Command, prefix: &str, out: &mut Vec<String>) {
        let subs: Vec<_> = cmd.get_subcommands().collect();
        if subs.is_empty() {
            if !prefix.is_empty() {
                out.push(prefix.to_string());
            }
            return;
        }
        for s in subs {
            // Skip clap's auto-generated "help" subcommand.
            if s.get_name() == "help" {
                continue;
            }
            let name = s.get_name();
            let p = if prefix.is_empty() {
                name.to_string()
            } else {
                format!("{prefix} {name}")
            };
            walk_leaves(s, &p, out);
        }
    }

    #[test]
    fn command_tiers_covers_every_leaf_subcommand() {
        use clap::CommandFactory;

        let root = Cli::command();
        let mut leaves: Vec<String> = Vec::new();
        walk_leaves(&root, "", &mut leaves);

        let known: std::collections::HashSet<&str> =
            COMMAND_TIERS.iter().map(|(cmd, _, _)| *cmd).collect();
        let skip: std::collections::HashSet<&str> = TIER_SKIP_LIST.iter().copied().collect();

        let mut missing: Vec<String> = Vec::new();
        for leaf in &leaves {
            if known.contains(leaf.as_str()) {
                continue;
            }
            if skip.contains(leaf.as_str()) {
                continue;
            }
            missing.push(leaf.clone());
        }

        assert!(
            missing.is_empty(),
            "leaf command(s) missing from COMMAND_TIERS (add to v1-surface-contract.toml\n \
             or, if genuinely local/operator-only, add to TIER_SKIP_LIST in src/main.rs):\n  {}",
            missing.join("\n  ")
        );
    }

